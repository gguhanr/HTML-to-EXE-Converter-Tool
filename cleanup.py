# =============================================================================
#  FILE        : cleanup.py
#  DESCRIPTION : Remove temporary build files after a successful build
#  AUTHOR      : BEST_TEAM
#  VERSION     : 3.0
#  USAGE       : python cleanup.py
# =============================================================================

# ─────────────────────────────────────────────────────────────────────────────
#  IMPORTS
# ─────────────────────────────────────────────────────────────────────────────
import os
import shutil

# ─────────────────────────────────────────────────────────────────────────────
#  APP INFO
# ─────────────────────────────────────────────────────────────────────────────
APP_AUTHOR  = "BEST_TEAM"
APP_VERSION = "v3.0"

# ─────────────────────────────────────────────────────────────────────────────
#  TERMINAL COLORS
# ─────────────────────────────────────────────────────────────────────────────
CY  = "\033[96m"
GR  = "\033[92m"
RD  = "\033[91m"
DIM = "\033[90m"
BLD = "\033[1m"
RST = "\033[0m"

# ─────────────────────────────────────────────────────────────────────────────
#  TARGETS TO REMOVE
# ─────────────────────────────────────────────────────────────────────────────
CLEANUP_TARGETS = [
    "_build_tmp",       # PyInstaller temp build directory
    "__pycache__",      # Python bytecode cache
]

# ─────────────────────────────────────────────────────────────────────────────
#  MAIN
# ─────────────────────────────────────────────────────────────────────────────
def main():

    # ── Banner ────────────────────────────────────────────────────────────
    print(f"""
{CY}{BLD}╔══════════════════════════════════════════════════════════╗
║                                                          ║
║      ⚡  HTML TO EXE CONVERTER  ·  CLEANUP  ·  v3.0     ║
║                                                          ║
║              Developed by  BEST_TEAM                     ║
║                                                          ║
╚══════════════════════════════════════════════════════════╝{RST}
""")

    # ── Remove targets ────────────────────────────────────────────────────
    removed = 0
    for target in CLEANUP_TARGETS:
        if os.path.isdir(target):
            shutil.rmtree(target)
            print(f"  {GR}[REMOVED]{RST}  {target}/")
            removed += 1
        elif os.path.isfile(target):
            os.remove(target)
            print(f"  {GR}[REMOVED]{RST}  {target}")
            removed += 1
        else:
            print(f"  {DIM}[ SKIP  ]{RST}  {target}  (not found)")

    # ── Result ────────────────────────────────────────────────────────────
    print(f"""
  {DIM}{'─' * 46}{RST}
  {GR}{BLD}[ ✓  CLEANUP DONE ]{RST}  {removed} item(s) removed.

  {DIM}Developed by {APP_AUTHOR}  ·  {APP_VERSION}{RST}
""")

# ─────────────────────────────────────────────────────────────────────────────
#  ENTRY POINT
# ─────────────────────────────────────────────────────────────────────────────
if __name__ == "__main__":
    main()
